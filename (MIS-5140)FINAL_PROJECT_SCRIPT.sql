USE A2ZSalonInc;

CREATE TABLE MEMBERSHIP_TYPE (
MT_ID INT PRIMARY KEY,
MT_NAME VARCHAR(50),
MT_DESCRIPT VARCHAR(255),
MT_PRICE DECIMAL(10,2),
MT_DURATION INT
);

CREATE TABLE SALON(
SALON_ID       INTEGER PRIMARY KEY,
SALON_AREACODE CHAR(5) NOT NULL,
SALON_PHONENUM CHAR(8) NOT NULL,
SALON_WEBSITE  VARCHAR(35) NOT NULL,
SALON_OPENING_TIME TIME,
SALON_CLOSING_TIME TIME
);

CREATE TABLE PRODUCT (
P_CODE 		VARCHAR (10) PRIMARY KEY, 
P_NAME 	    VARCHAR (35) NOT NULL,
P_QOH   	INTEGER NOT NULL,
P_MIN 		INTEGER NOT NULL,
P_PRICE 	NUMERIC (8,2) NOT NULL,
P_DISCOUNT 	NUMERIC (4,2) NOT NULL,
);

CREATE TABLE PROMOTIONS (
PROMO_CODE VARCHAR(35) PRIMARY KEY,
PROMO_TYPE VARCHAR(35) NOT NULL,
PROMO_VALIDITYDATE DATETIME NOT NULL,
MT_ID INTEGER NOT NULL,
FOREIGN KEY (MT_ID) REFERENCES MEMBERSHIP_TYPE(MT_ID)
);

CREATE TABLE CUSTOMER (
CUST_ID          INTEGER PRIMARY KEY,
CUST_FNAME       VARCHAR(15) NOT NULL,
CUST_LNAME       VARCHAR(15) NOT NULL,
CUST_PHONENUM    CHAR(5) NOT NULL,
CUST_AREACODE    CHAR(5) NOT NULL,
MT_ID            INTEGER,
FOREIGN KEY (MT_ID) REFERENCES MEMBERSHIP_TYPE(MT_ID)
);

CREATE TABLE BEAUTICIAN(
BEAUT_ID         INTEGER PRIMARY KEY,
BEAUT_LNAME      VARCHAR (15) NOT NULL,
BEAUT_FNAME      VARCHAR (15) NOT NULL,
BEAUT_DOB        DATETIME NOT NULL,
BEAUT_HIRE_DATE DATETIME DEFAULT GETDATE (),
BEAUT_AREACODE   CHAR (5) NOT NULL,
BEAUT_PHONENO    CHAR (5) NOT NULL,
BEAUT_PAYRATE    NUMERIC,
BEAUT_MGR        INTEGER,
SALON_ID         INTEGER NOT NULL,
FOREIGN KEY (SALON_ID) REFERENCES SALON(SALON_ID),
FOREIGN KEY (BEAUT_MGR) REFERENCES BEAUTICIAN(BEAUT_ID)
);

CREATE TABLE CERTIFICATIONS (
CP_ID INTEGER PRIMARY KEY,
CP_TYPE VARCHAR(35) NOT NULL,
BEAUT_ID  INTEGER NOT NULL,
FOREIGN KEY (BEAUT_ID) REFERENCES BEAUTICIAN(BEAUT_ID)
);

CREATE TABLE REWARD_POINTS(
REWARD_ID INTEGER PRIMARY KEY,
REWARd_TITLE VARCHAR(35) NOT NULL,
BEAUT_ID INTEGER  NOT NULL,
FOREIGN KEY (BEAUT_ID) REFERENCES BEAUTICIAN(BEAUT_ID)
);

CREATE TABLE SALON_SERVICES (
SERVICE_ID         INTEGER PRIMARY KEY, 
SERVICE_NAME       VARCHAR(35) NOT NULL,
SERVICE_PRICE      NUMERIC NOT NULL, 
SERVICE_TYPE       VARCHAR (50) NOT NULL,
CUST_ID            INTEGER NOT NULL,
BEAUT_ID           INTEGER NOT NULL,
FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID),
FOREIGN KEY (BEAUT_ID) REFERENCES BEAUTICIAN(BEAUT_ID)
);

CREATE TABLE APPOINTMENTS (
APP_ID        INTEGER PRIMARY KEY,
APP_DATETIME DATETIME NOT NULL,
APP_TYPE    VARCHAR(35) NOT NULL,
CUST_ID       INTEGER NOT NULL,
SALON_ID INTEGER NOT NULL,
FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID),
FOREIGN KEY (SALON_ID) REFERENCES SALON(SALON_ID),
);

CREATE TABLE INVOICE (
INVOICE_NUM INTEGER PRIMARY KEY,
INVOICE_DATE DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
CUST_ID INTEGER NOT NULL,
SALON_ID INTEGER NOT NULL,
FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID),
FOREIGN KEY (SALON_ID) REFERENCES SALON(SALON_ID),
);

CREATE TABLE CREDITCARD (
CC_NUM INTEGER PRIMARY KEY,
CC_EXPDATE DATE NOT NULL,
ISSUER VARCHAR(35) NOT NULL,
CUST_ID INTEGER NOT NULL,
FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID)
);

CREATE TABLE CHECKS (
CHECK_NUM INTEGER PRIMARY KEY,
CHECK_ACC_NUM INTEGER NOT NULL,
CHECK_ROUTING_NUM INTEGER NOT NULL,
CHECK_AMT INTEGER NOT NULL,
CHECK_DATE DATE NOT NULL,
CUST_ID INTEGER NOT NULL,
FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID)
);

CREATE TABLE CASH(
CASH_RECEIPT_NUM INTEGER PRIMARY KEY,
AMOUNT INTEGER NOT NULL,
CUST_ID INTEGER NOT NULL,
FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID)
);

CREATE TABLE Check_Payment (
INVOICE_NUM INT NOT NULL,
CHECK_NUM INT NOT NULL,
Payment_Date DATE NOT NULL,
Payment_Amount DECIMAL(10, 2) NOT NULL,
PRIMARY KEY(INVOICE_NUM,CHECK_NUM),
FOREIGN KEY (INVOICE_NUM) REFERENCES INVOICE(INVOICE_NUM) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (CHECK_NUM) REFERENCES CHECKS(CHECK_NUM) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE CC_PAYMENT(
INVOICE_NUM INT NOT NULL,
CC_NUM INT NOT NULL,
CC_PAYMENT_DATE DATE NOT NULL,
CC_PAYMENT_AMT DECIMAL(10, 2) NOT NULL,
PRIMARY KEY(INVOICE_NUM,CC_NUM),
FOREIGN KEY (INVOICE_NUM) REFERENCES INVOICE(INVOICE_NUM) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (CC_NUM) REFERENCES CREDITCARD(CC_NUM) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE CASH_PAYMENT(
INVOICE_NUM INT NOT NULL,
CASH_RECEIPT_NUM INT,
CASH_PAYMENT_DATE DATE NOT NULL,
CASH_PAYMENT_AMT DECIMAL(10, 2) NOT NULL,
FOREIGN KEY (INVOICE_NUM) REFERENCES INVOICE(INVOICE_NUM) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (CASH_RECEIPT_NUM) REFERENCES CASH(CASH_RECEIPT_NUM) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE LINE(
INVOICE_NUM 	INTEGER NOT NULL,
LINE_NUMBER	NUMERIC(2,0) NOT NULL,
P_CODE		VARCHAR(10),
SERVICE_ID INTEGER,
LINE_UNITS	NUMERIC(9,2) DEFAULT 0.00 NOT NULL,
LINE_PRICE	NUMERIC(9,2) DEFAULT 0.00 NOT NULL,
PRIMARY KEY (INVOICE_NUM,LINE_NUMBER),
FOREIGN KEY (INVOICE_NUM) REFERENCES INVOICE ON DELETE CASCADE,
FOREIGN KEY (P_CODE) REFERENCES PRODUCT(P_CODE),
FOREIGN KEY (SERVICE_ID) REFERENCES SALON_SERVICES(SERVICE_ID),
);

